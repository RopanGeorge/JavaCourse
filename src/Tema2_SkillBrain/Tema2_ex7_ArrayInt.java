package Tema2_SkillBrain;

public class Tema2_ex7_ArrayInt {
    public static void main(String[] args) {
        int[] numere=new int[]{2,3,4,5,1};
        int suma=0;
        for(int i=0;i< numere.length;i++)
        {
            suma+=numere[i];


        }
        System.out.println("Suma "+suma);
    }
}
